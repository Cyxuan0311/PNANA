std::vector<std::string> LogoManager::getLogoLines(const std::string& style_id) {
    auto it = LOGO_MAP.find(style_id);
    if (it != LOGO_MAP.end()) {
        return it->second;
    }
    return LOGO_BLOCK;
}

bool LogoManager::isValidStyle(const std::string& style_id) {
    return LOGO_MAP.find(style_id) != LOGO_MAP.end();
}

